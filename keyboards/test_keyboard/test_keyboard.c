void init_timer(void){
   oled_logo_timer = timer_read32();
};

char up_str[] = "UP";
char down_str[] = "DOWN";
char left_str[] = "LEFT";
char right_str[] = "RIGHT";

char pressed_str[] = "pressed";
char released_str[] = "released";

char* action_str_p = NULL;
char* key_str_p = NULL;

void user_oled_magic(void) {
    oled_write_P(PSTR("Ciaoone "), false);
    oled_write_P(PSTR("1 "), false);
    oled_write_P(PSTR("2 "), false);
    oled_write_ln_P(PSTR("3"), false);

    if (action_str_p && key_str_p) {
        oled_write_P(PSTR("Last key "), false);
        oled_write_ln_P(PSTR(action_str_p), true);
        oled_write_P(PSTR(" is "), false);
        oled_write_ln_P(PSTR(key_str_p), true);
    }
}

void render_logo(void) {
    oled_write_raw_P(my_logo, sizeof(my_logo));
}

void clear_screen(void) {
    if (clear_logo){
      for (uint8_t i = 0; i < OLED_DISPLAY_HEIGHT; ++i) {
        for (uint8_t j = 0; j < OLED_DISPLAY_WIDTH; ++j) {
          oled_write_raw_byte(0x0, i*OLED_DISPLAY_WIDTH + j);
        }
      }
      clear_logo = false;
    }
}

oled_rotation_t oled_init_kb(oled_rotation_t rotation) {
    return OLED_ROTATION_180;
}


void keyboard_post_init_kb(void) {
    init_timer();
    init_uart_print();
    log_print("ciaoo %d\n", 2);

    keyboard_post_init_user();
}

bool     process_record_kb(uint16_t keycode, keyrecord_t *record) {

    switch (keycode)
    {
    case KC_LEFT:
        action_str_p = record->event.pressed ? pressed_str : released_str;
        key_str_p = left_str;
        log_print("LEFT %d\n", record->event.pressed);
        break;
    case KC_UP:
        action_str_p = record->event.pressed ? pressed_str : released_str;
        key_str_p = up_str;
        log_print("UP %d\n", record->event.pressed);
        break;
    case KC_RIGHT:
        action_str_p = record->event.pressed ? pressed_str : released_str;
        key_str_p = right_str;
        log_print("RIGHT %d\n", record->event.pressed);
        break;
    case KC_DOWN:
        action_str_p = record->event.pressed ? pressed_str : released_str;
        key_str_p = down_str;
        log_print("DOWN %d\n", record->event.pressed);
        break;
    
    default:
        break;
    }

    return process_record_user(keycode, record);
}


#    define SHOW_LOGO 5000
bool oled_task_kb(void) {
    if (!oled_task_user()) { return false; }
    if ((timer_elapsed32(oled_logo_timer) < SHOW_LOGO)){
        render_logo();
    }else{
      clear_screen();
      user_oled_magic();
    }
    return false;
}



void raw_hid_receive(uint8_t *data, uint8_t length) {
    log_print("Recv %d\n", length);
    raw_hid_send(data, length);
}